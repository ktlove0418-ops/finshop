  <script type="module">
    dayjs.locale('th');
    const {
      createApp
    } = Vue;
    createApp({
      data() {
        return {
          auth: "",
          cateNameError: false,
          textError: "",
          cateName: "",
          noted: "",
          dataCategory: "",
          dataRoles:'',
          employee:''
        };
      },

      mounted() {
        const profile = JSON.parse(localStorage.getItem("Fin-Profile"));

        if (!profile || profile === "undefined") {
          // ตรวจสอบว่าค่าเป็น null, undefined หรือ "undefined"
          Swal.fire({
            toast: true,
            position: "top-end", // ตำแหน่งของ Toast
            icon: "warning", //ไอคอน (success, error, warning, info, question)
            title: "session die !", // ข้อความหลัก
            showConfirmButton: false, // ไม่แสดงปุ่มยืนยัน
            timer: 3000, // ระยะเวลาแสดง (ms)
            timerProgressBar: true, // แสดงแถบเวลา
          });
          window.location = "../";
        } else {
          this.getEmployeeRoles(profile.data.id);
          this.getType();
        }
      },
      methods: {
        async getEmployeeRoles(empId) {
          const res = await axios.post("../../api/", {
            post: "get_employee_roles",
            employee_id: empId
          });
          if (res.data.status) {
            this.employee = res.data.employee;
            this.dataRoles = res.data.roles;
          }
        },
        formatThaiDate(dateStr) {
          return dayjs(dateStr).format("D MMM YY เวลา HH:mm");
        },

        showToast(txt, icn) {
          wal.fire({
            toast: true,
            position: "top-end", // ตำแหน่งของ Toast
            icon: icn, // ไอคอน (success, error, warning, info, question)
            title: txt, // ข้อความหลัก
            showConfirmButton: false, // ไม่แสดงปุ่มยืนยัน
            timer: 5000, // ระยะเวลาแสดง (ms)
            timerProgressBar: true, // แสดงแถบเวลา
          });
        },

        saveData: async function() {
          if (this.cateName == '') {
            this.cateNameError = true;
            this.textError = 'กรุณากรอกชื่อประเภทสินค้า!';
            return false;
          }

          try {
            const url = '../../api/';
            const config = {
              headers: {
                'Content-Type': 'application/json'
              }
            };

            const response = await axios.post(url, {
              post: 'save_type',
              cateName: this.cateName,
              noted: this.noted,
            }, config);

            if (response.data.status) {
              Swal.fire({
                toast: true,
                position: "top-end",
                icon: 'success',
                title: response.data.message,
                showConfirmButton: false,
                timer: 3000,
                timerProgressBar: true,
              });

              this.getType();
            } else {
              Swal.fire({
                toast: true,
